<script>
    $(document).ready(function() {
        // Hide all questions except the first one
        $('.question').not(':first').hide();

        // Next button click event
        $('.next-btn').on('click', function() {
            // Get the current question
            var currentQuestion = $(this).closest('.question');

            // Hide the current question
            currentQuestion.hide();

            // Get the next question
            var nextQuestion = currentQuestion.next('.question');

            // If there is a next question, show it
            if (nextQuestion.length !== 0) {
                nextQuestion.show();
            }
        });

        // Previous button click event
        $('.previous-btn').on('click', function() {
            // Get the current question
            var currentQuestion = $(this).closest('.question');

            // Hide the current question
            currentQuestion.hide();

            // Get the previous question
            var prevQuestion = currentQuestion.prev('.question');

            // If there is a previous question, show it
            if (prevQuestion.length !== 0) {
                prevQuestion.show();
            } else {
                // If there is no previous question, show the last question
                $('.question').last().show();
            }
        });
    });
</script>